async def test_overlap_patterns(
    test_client: AsyncClient,
    graphql_client: GraphQLClient,
    default_org_unit: TestingCreateOrgUnitOrgUnitCreate,
    default_employee: TestingCreateEmployeeEmployeeCreate,
) -> None:
    """
    This case tests different patterns of overlapping validities.

    Before event (Arrange):

    ```
        time         t1....t2....t3....t4....t5....t6....t7....t8....t9....t10...t11...t12...t13...t14...t15...t16...t17...t18
        manager      |-----------------------------------------|           |-----------------|           |-----|
        engagement1        |-----|     |-----------|                 |-----------|     |-----------|                 |-----|
        engagement2                          |-----------|
    ```

    After event (Assert):

    ```
        manager            |-----|     |-----------------|                 |-----|     |-----|
        engagement1        |-----|     |-----------|                 |-----------|     |-----------|                 |-----|
        engagement2                          |-----------|
    ```
    """

    # Arrange

    tz = ZoneInfo("Europe/Copenhagen")
    t1 = datetime(2001, 1, 1, tzinfo=tz)
    t2 = datetime(2002, 1, 1, tzinfo=tz)
    t3 = datetime(2003, 1, 1, tzinfo=tz)
    t4 = datetime(2004, 1, 1, tzinfo=tz)
    t5 = datetime(2005, 1, 1, tzinfo=tz)
    t6 = datetime(2006, 1, 1, tzinfo=tz)
    t7 = datetime(2007, 1, 1, tzinfo=tz)
    t8 = datetime(2008, 1, 1, tzinfo=tz)
    t9 = datetime(2009, 1, 1, tzinfo=tz)
    t10 = datetime(2010, 1, 1, tzinfo=tz)
    t11 = datetime(2011, 1, 1, tzinfo=tz)
    t12 = datetime(2012, 1, 1, tzinfo=tz)
    t13 = datetime(2013, 1, 1, tzinfo=tz)
    t14 = datetime(2014, 1, 1, tzinfo=tz)
    t15 = datetime(2015, 1, 1, tzinfo=tz)
    t16 = datetime(2016, 1, 1, tzinfo=tz)
    t17 = datetime(2017, 1, 1, tzinfo=tz)
    t18 = datetime(2018, 1, 1, tzinfo=tz)

    engagement_type = uuid.uuid4()
    job_function = uuid.uuid4()

    # create engagement1 with 5 validities: [t2,t3], [t4,t6], [t9,t11], [t12,t14], [t17,t18]
    eng1_uuid = uuid.uuid4()
    for from_, to in [(t2, t3), (t4, t6), (t9, t11), (t12, t14), (t17, t18)]:
        await graphql_client._testing__create_engagement(
            EngagementCreateInput(
                uuid=eng1_uuid,
                validity=RAValidityInput(from_=from_, to=to),
                org_unit=default_org_unit.uuid,
                engagement_type=engagement_type,
                job_function=job_function,
                person=default_employee.uuid,
            )
        )

    # create engagement2 with validity [t5, t7]
    await graphql_client._testing__create_engagement(
        EngagementCreateInput(
            validity=RAValidityInput(from_=t5, to=t7),
            org_unit=default_org_unit.uuid,
            engagement_type=engagement_type,
            job_function=job_function,
            person=default_employee.uuid,
        )
    )

    # create manager with 3 validities: [t1,t8], [t10,t13], [t15,t16]
    manager_uuid = uuid.uuid4()
    responsibility = [uuid.uuid4()]
    manager_level = uuid.uuid4()
    manager_type = uuid.uuid4()
    for from_, to in [(t1, t8), (t10, t13), (t15, t16)]:
        await graphql_client._testing__create_manager(
            ManagerCreateInput(
                uuid=manager_uuid,
                validity=RAValidityInput(from_=from_, to=to),
                person=default_employee.uuid,
                responsibility=responsibility,
                org_unit=default_org_unit.uuid,
                manager_level=manager_level,
                manager_type=manager_type,
            )
        )

    # Act

    await test_client.post("/events/engagement")

    # Assert

    @retry(stop=stop_after_delay(5))
    async def verify() -> None:
        managers = await graphql_client.get_managers(
            filter=ManagerFilter(
                uuids=[manager_uuid],
                from_date=None,
                to_date=None,
            )
        )
        manager_obj = one(managers.objects)
        validities = sorted(manager_obj.validities, key=lambda v: v.validity.from_)
        assert len(validities) == 4
        assert validities[0].validity.from_ == t2
        assert validities[0].validity.to == t3
        assert validities[1].validity.from_ == t4
        assert validities[1].validity.to == t7
        assert validities[2].validity.from_ == t10
        assert validities[2].validity.to == t11
