async def get_by_employee_uuids(
    mo: GraphQLClient, employee_uuids: list[UUID]
) -> list[GetManagersManagersObjects]:
    gql_response = await mo.get_managers(
        ManagerFilter(
            employee=EmployeeFilter(
                uuids=employee_uuids,
            ),
            from_date=None,
            to_date=None,
        )
    )
    return gql_response.objects


def invalid_manager_periods(
    managers: list[GetManagersManagersObjects],
) -> list[InvalidManagerPeriod]:
    """Returns periods where the given managers are invalid.

    An invalid manager period is a period where a manager validity is ACTIVE,
    but the related manager-employee does not have any active engagement. The
    rationale being that a person needs to be employed in order to be a manager.

    # Example

    Assume a person has two manager roles and two engagements:
    ```
    manager1             |-----------------------------------|
    manager2                                           |-----------------|
    engagement1                |-----------|
    engagement2                      |-----------|     |-----------|
    ---
    manager1 is invalid  |=====|                 |=====|
    manager2 is invalid                                            |=====|
    ```
    """

    return [
        period
        for manager_obj in managers
        for period in _find_invalid_periods_for_one_manager(manager_obj)
    ]


def _find_invalid_periods_for_one_manager(
    manager_obj: GetManagersManagersObjects,
) -> list[InvalidManagerPeriod]:
    invalid_manager_periods = []
    manager_timeline = _make_manager_timeline(manager_obj)
    engagement_timelines = _make_engagement_timelines(manager_obj)

    manager_endpoints = manager_timeline.get_interval_endpoints()
    engagement_endpoints = set(
        collapse(timeline.get_interval_endpoints() for timeline in engagement_timelines)
    )
    endpoints = sorted(manager_endpoints.union(engagement_endpoints))

    for start, end in pairwise(endpoints):
        try:
            manager_value: ManagerValue | None = manager_timeline.entity_at(start).value  # type: ignore[assignment]  # mypy doesn't understand generics
        except NoValueError:
            manager_value = None

        engagement_active = _any_engagement_active_at(engagement_timelines, start)

        if (
            manager_value
            and manager_value.status == ManagerStatus.ACTIVE
            and not engagement_active
        ):
            invalid_manager_periods.append(
                InvalidManagerPeriod(uuid=manager_value.uuid, from_=start, to=end)
            )

    return invalid_manager_periods


def _make_manager_timeline(
    manager_obj: GetManagersManagersObjects,
) -> Timeline[Manager]:
    manager_intervals = tuple(
        Manager(
            start=manager_validity.validity.from_,
            end=manager_validity.validity.to or POSITIVE_INFINITY,
            value=ManagerValue(
                uuid=manager_validity.uuid,
                status=ManagerStatus.ACTIVE
                if manager_validity.person_response
                else ManagerStatus.VACANT,
            ),
        )
        for manager_validity in manager_obj.validities
    )
    manager_intervals_combined = combine_intervals(manager_intervals)
    return Timeline(intervals=manager_intervals_combined)


def _make_engagement_timelines(
    manager_obj: GetManagersManagersObjects,
) -> list[Timeline[Active]]:
    # get a list of engagement objects, each of which has multiple validities
    engagements = (
        engagement_obj
        for manager in manager_obj.validities
        if manager.person_response
        for manager_employee in manager.person_response.validities
        for engagement_obj in (manager_employee.engagements_response.objects or [])
        if engagement_obj
    )

    # turn each engagement object into a timeline
    timelines = []
    for engagement_obj in engagements:
        engagement_intervals = tuple(
            Active(
                start=engagement_validity.validity.from_,
                end=engagement_validity.validity.to or POSITIVE_INFINITY,
                value=True,
            )
            for engagement_validity in engagement_obj.validities
        )
        if not engagement_intervals:
            # there's no point in including empty timelines
            continue  # pragma: no cover
        timeline: Timeline[Active] = Timeline(
            intervals=combine_intervals(engagement_intervals)
        )
        timelines.append(timeline)

    return timelines


def _any_engagement_active_at(
    timelines: list[Timeline[Active]], timestamp: datetime
) -> bool:
    """Checks if any of the given timelines are active at the given timestamp."""
    for timeline in timelines:
        try:
            active = timeline.entity_at(timestamp)
        except NoValueError:
            continue
        if active.value:
            return True
    return False


async def terminate_manager_periods(
    mo: GraphQLClient, periods: list[InvalidManagerPeriod]
) -> list[TerminateManagerManagerTerminate]:
    terminated_manager_periods = []
    for period in periods:
        terminate_args: dict[str, Any] = {
            "uuid": period.uuid,
            "terminate_from": period.from_.date(),
            "terminate_to": period.to.date(),
        }
        if period.to is POSITIVE_INFINITY:
            terminate_args["terminate_from"] = None
            terminate_args["terminate_to"] = period.from_.date()
        try:
            terminated_manager_periods.append(
                await mo.terminate_manager(**terminate_args)
            )
        except Exception as e:  # pragma: no cover
            logger.error(
                "Failed to terminate invalid manager period:",
                period=json.dumps(jsonable_encoder(period)),
            )
            raise e

    return terminated_manager_periods


async def update_manager_to_vacant(
    mo: GraphQLClient, periods: list[InvalidManagerPeriod]
) -> list[UpdateManagerManagerUpdate]:
    updated_manager_periods = []
    for period in periods:
        update_args: dict[str, Any] = {
            "uuid": period.uuid,
            "vacant_from": period.from_.date(),
            "vacant_to": period.to.date(),
        }

        if period.to is POSITIVE_INFINITY:
            update_args["vacant_to"] = None
            update_args["vacant_from"] = period.from_.date()

        try:
            updated_manager_periods.append(await mo.update_manager(**update_args))
        except Exception as e:  # pragma: no cover
            logger.error(
                "Failed to update invalid manager period to vacant:",
                period=json.dumps(jsonable_encoder(period)),
            )
            raise e

    return updated_manager_periods
